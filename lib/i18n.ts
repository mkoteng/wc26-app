/** Translate a WC best result string, e.g. "Champions (2018)" → "Verdensmestre (2018)" */
export function translateWcResult(result: string, prefixes: Record<string, string>): string {
  for (const [key, val] of Object.entries(prefixes)) {
    if (result.startsWith(key)) return val + result.slice(key.length)
  }
  return result
}
